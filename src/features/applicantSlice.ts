// Define the slice using the createSlice function
const applicantSlice = createSlice({
  name: 'applicant',
  initialState,
  reducers: {
    // Define additional actions here
    updateFirstName: (state, action: PayloadAction<string>) => {
      state.data.first_name = action.payload
    },
    updateLastName: (state, action: PayloadAction<string>) => {
      state.data.last_name = action.payload
    },
    updateEmail: (state, action: PayloadAction<string>) => {
      state.data.email = action.payload
    },
    updateSkills: (state, action: PayloadAction<string[]>) => {
      state.data.skills = action.payload
    },
    updateHobbies: (state, action: PayloadAction<string[]>) => {
      state.data.hobbies = action.payload
    },
  },
  // Define the extra reducers that handle the async actions
  extraReducers: (builder) => {
    builder
      // Handle the "pending" action
      .addCase(fetchApplicant.pending, (state) => {
        state.status = 'loading'
      })
      // Handle the "fulfilled" action
      .addCase(
        fetchApplicant.fulfilled,
        (state, action: PayloadAction<any>) => {
          state.status = 'succeeded'
          state.data = { ...action.payload, id: action.payload.id }
          state.data = action.payload
        }
      )
      // Handle the "rejected" action
      .addCase(
        fetchApplicant.rejected,
        (state, action: PayloadAction<unknown, string, any, any>) => {
          state.status = 'failed'
          state.error = action.payload as string
        }
      )
  },
})

// Export the reducer and any action creators generated by createSlice
export const {
  updateFirstName,
  updateLastName,
  updateEmail,
  updateSkills,
  updateHobbies,
} = applicantSlice.actions
export default applicantSlice.reducer
